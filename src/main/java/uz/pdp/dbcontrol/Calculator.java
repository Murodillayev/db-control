package uz.pdp.dbcontrol;

public class Calculator {

    public double add(double a, double b) {
        return a + b;
    }

    public double sub(double a, double b) {
        return a - b;
    }

    public double mul(double a, double b) {
        return a * b;
    }

    public int div(int a, int b) {
        return a / b;
    }
}
